num1 = int(input("Enter your first number : "))
num2 = int(input("Enter your second number : "))
symbol = input("What you want to do?\n")

if ( symbol == "+" ):
    print( num1 + num2)

elif ( symbol == "-" ):
    print( num1 - num2)

elif ( symbol == "*" ):
    print( num1 * num2)

elif ( symbol == "/" ):
    print( num1 / num2)